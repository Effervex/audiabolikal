package audiabolikal.equipment;

/**
 * A weapon for a character, in an abstract sense. A weapon can have multiple
 * attacks, regardless of its form.
 * 
 * @author Samuel J. Sarjant
 */
public abstract class Weapon extends Item {
	/** The animation for a downward vertical attack. */
	
	/** The animation for a forehand horizontal attack. */
	
	/** The animation for a backhand horizontal attack. */
	
	/** The animation for a TL-BR diagonal attack. */
	
	/** The animation for a TR-BL diagonal attack. */
	
	/** The animation for a thrusting attack. */
	
	/** The animation for a magic shot attack. */
	
	public Weapon() {
		className_ = "Weapon";
	}
}
